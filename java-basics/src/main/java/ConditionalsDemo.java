public class ConditionalsDemo {

    public static void main(String[] args) {

        int length = 5;
        double width =2.5;

        double area = length * width;

        if (area > 10){
            System.out.println("The area is greater than 10");
        } else {
            System.out.println("The area is less than 10");
        }


    }
}
